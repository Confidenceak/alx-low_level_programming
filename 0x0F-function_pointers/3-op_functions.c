#include "calc.h"

/**
 * op_add - function that sum of two int values
 * @a: first integer value
 * @b: second integer value
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtract of two int values
 * @a: first integer value
 * @b: second integer value
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiply of two int values
 * @a: first integer value
 * @b: second integer value
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two int values
 * @a: first integer value
 * @b: second integer value
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that gives the modulus of two int values
 * @a: first integer value
 * @b: second integer value
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
