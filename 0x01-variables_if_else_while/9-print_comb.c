#include <stdio.h>
/**
 * main - Entry point
 * Description: 'prints all possible combinations of single-digit numbers'
 * Return: always 0 (success)
 */
int main(void)
{
	int n;

	for (n = 48; n <= 57; n++)
	{
		while (n = 57)
		{
			putchar(n);
		}
		putchar(',');
		putchar(' ');
	}
	return (0);
}
